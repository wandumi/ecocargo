     */
    public function store(Request $request)
    {
        // return $request->all();
        $this->validate($request, [
            'state' => 'required',
            'name' => 'required|unique:cities,name'
        ]);

        $state = new City;
        $state->state_id = $request->state;
        $state->name = $request->name;
        $state->save();

        return response()->Json('Successfully', 200);
    }

